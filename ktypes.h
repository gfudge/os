typedef unsigned short  k_uint16_t;
typedef unsigned int    k_uint32_t;

typedef k_uint32_t      k_size_t;

// Pointer Types
typedef unsigned long   *k_ptr_t;

// Char/String Types
typedef char            k_char_t;
typedef k_char_t        *k_str_t;

#endif /* _KTYPES_H */
